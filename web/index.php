        function items_list() {
            var tags = $(".info_choice .close_tag");
            var url = [];
            for (var i = 0; i < tags.length; i++) {
                url.push("tag[]=" + tags.eq(i).attr("data"));
            }
            str = "?" + url.join("&");
            return str;
        }

